# Write config file
cat > "$CONFIG_FILE" << EOF
# Free AI CLI — API Keys (all free, no credit card)
# Generated by setup-free-ai.sh

${GOOGLE_KEY:+export GOOGLE_AI_API_KEY="$GOOGLE_KEY"}
${GROQ_KEY:+export GROQ_API_KEY="$GROQ_KEY"}
${MISTRAL_KEY:+export MISTRAL_API_KEY="$MISTRAL_KEY"}
${CEREBRAS_KEY:+export CEREBRAS_API_KEY="$CEREBRAS_KEY"}
${OPENROUTER_KEY:+export OPENROUTER_API_KEY="$OPENROUTER_KEY"}
${DEEPSEEK_KEY:+export DEEPSEEK_API_KEY="$DEEPSEEK_KEY"}
