    echo -e "  ${YELLOW}⬜ Google AI Studio — not configured${NC}"
fi

if [[ -n "$GROQ_API_KEY" ]]; then
    echo -e "  ${GREEN}✅ Groq${NC}"
    ((CONFIGURED++))
else
    echo -e "  ${YELLOW}⬜ Groq — not configured${NC}"
