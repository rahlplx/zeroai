    echo -e "  ${GREEN}✅ Cerebras${NC}"
    ((CONFIGURED++))
else
    echo -e "  ${YELLOW}⬜ Cerebras — not configured${NC}"
fi

if [[ -n "$OPENROUTER_API_KEY" ]]; then
    echo -e "  ${GREEN}✅ OpenRouter${NC}"
    ((CONFIGURED++))
else
    echo -e "  ${YELLOW}⬜ OpenRouter — not configured${NC}"
fi

if [[ -n "$DEEPSEEK_API_KEY" ]]; then
    echo -e "  ${GREEN}✅ DeepSeek${NC}"
    ((CONFIGURED++))
else
    echo -e "  ${YELLOW}⬜ DeepSeek — not configured${NC}"
fi

if command -v ollama &>/dev/null; then
    echo -e "  ${GREEN}✅ Ollama (local, unlimited)${NC}"
    ((CONFIGURED++))
else
    echo -e "  ${YELLOW}⬜ Ollama — not installed${NC}"
fi

echo ""
