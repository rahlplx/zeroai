SHELL_RC="$HOME/.bashrc"
if [[ -f "$HOME/.zshrc" ]]; then
    SHELL_RC="$HOME/.zshrc"
fi

if ! grep -q "free-ai-env" "$SHELL_RC" 2>/dev/null; then
    echo "" >> "$SHELL_RC"
    echo "# Free AI CLI" >> "$SHELL_RC"
    echo "source ~/.free-ai-env" >> "$SHELL_RC"
