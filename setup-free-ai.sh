if command -v ollama &>/dev/null; then
    echo -e "  ${GREEN}✅ Ollama already installed${NC}"
else
    echo -e "  ${YELLOW}Installing Ollama for unlimited local AI...${NC}"
    curl -fsSL https://ollama.com/install.sh | sh 2>/dev/null || {
        echo -e "  ${YELLOW}⬜ Ollama install skipped (install manually from https://ollama.com)${NC}"
    }
fi

# Pull a small model for instant local AI
if command -v ollama &>/dev/null; then
    echo -e "  ${GRAY}Pulling Qwen3 8B (fast, good quality, ~5GB)...${NC}"
    ollama pull qwen3:8b 2>/dev/null &
    OLLAMA_PULL_PID=$!
fi
echo ""

# ─── Step 3: Get FREE API Keys ────────────────────────────────────────
echo -e "${BOLD}🔑 Step 3: Get your FREE API keys${NC}"
echo -e "${GRAY}Open these URLs in your browser and generate free API keys:${NC}"
